#ifndef ZEPHYR_INCLUDE_DT_BINDINGS_PINCTRL_ANSILIC_AS32X601_PINCTRL_H_
#define ZEPHYR_INCLUDE_DT_BINDINGS_PINCTRL_ANSILIC_AS32X601_PINCTRL_H_

#define AS32X601_PORT_A 0U
#define AS32X601_PORT_B 1U
#define AS32X601_PORT_C 2U
#define AS32X601_PORT_D 3U
#define AS32X601_PORT_E 4U
#define AS32X601_PORT_F 5U
#define AS32X601_PORT_G 6U

#define AS32X601_GPIO_AF_PIN_X   0U
#define AS32X601_GPIO_AF_USART0  2U
#define AS32X601_GPIO_AF_USART1  4U
#define AS32X601_GPIO_AF_USART2  2U
#define AS32X601_GPIO_AF_USART3  2U
#define AS32X601_GPIO_AF_USART4  2U
#define AS32X601_GPIO_AF_USART5  4U
#define AS32X601_GPIO_AF_USART6  3U
#define AS32X601_GPIO_AF_USART7  3U
#define AS32X601_GPIO_AF_SPI4    3U
#define AS32X601_GPIO_AF_IIC0_M1 2U
#define AS32X601_GPIO_AF_IIC1    2U

#define AS32X601_GPIO_DRIVE_4_5MA  0U
#define AS32X601_GPIO_DRIVE_9MA    1U
#define AS32X601_GPIO_DRIVE_13_5MA 2U
#define AS32X601_GPIO_DRIVE_18MA   3U

#define AS32X601_PIN_PORT_POS        0U
#define AS32X601_PIN_NUM_POS         4U
#define AS32X601_PIN_AF_POS          8U
#define AS32X601_PIN_PULL_UP_POS     11U
#define AS32X601_PIN_PULL_DOWN_POS   12U
#define AS32X601_PIN_OPEN_DRAIN_POS  13U
#define AS32X601_PIN_PUSH_PULL_POS   14U
#define AS32X601_PIN_OUTPUT_HIGH_POS 15U
#define AS32X601_PIN_OUTPUT_LOW_POS  16U
#define AS32X601_PIN_DRIVE_POS       17U
#define AS32X601_PIN_INPUT_ENABLE_POS 19U
#define AS32X601_PIN_OUTPUT_ENABLE_POS 20U

#define AS32X601_PIN_PORT_MASK       0xFU
#define AS32X601_PIN_NUM_MASK        0xFU
#define AS32X601_PIN_AF_MASK         0x7U
#define AS32X601_PIN_DRIVE_MASK      0x3U

#define AS32X601_PINMUX(port, pin, af) \
	((((port) & AS32X601_PIN_PORT_MASK) << AS32X601_PIN_PORT_POS) | \
	 (((pin) & AS32X601_PIN_NUM_MASK) << AS32X601_PIN_NUM_POS) | \
	 (((af) & AS32X601_PIN_AF_MASK) << AS32X601_PIN_AF_POS))

#endif
